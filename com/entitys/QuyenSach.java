    void setIntoTable() {
        QuyenSach quyensach = new QuyenSach();
        //set kieu cho doi tuong 
        //add vao table
//        quyensach.setId(Integer.parseInt(maSach.getText()));
        quyensach.setName(txt_tenSach.getText());
        quyensach.setMoney(Float.parseFloat(txt_giaTien.getText()));
        quyensach.setKieu(txt_theLoai.getText());
        try {
            quyensach.setDob(new SimpleDateFormat("dd/mm/yyyy").parse(txt_nxb.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(QuyenSach.class.getName()).log(Level.SEVERE, null, ex);
        }
        list.add(quyensach);
    }

    private void ShowResult() {
        //add vao table(1)
        QuyenSach quyensach = list.get(list.size() - 1);
        model.addRow(new Object[]{
            quyensach.getId(), quyensach.getName(), quyensach.getKieu(), quyensach.getDob(), quyensach.getMoney()
        });
    }

